    <div class="box-body">
        <p>The SGA Initiative Tracker is the place where the UMBC community and general public can
            educate themselves about the work being done by active members of the UMBC Student
            Government Association on behalf of the undergraduate student body.</p>
        <p>Envisioned in Fall 2014 by Justin Chavez '17 as a project to educate the average student
            about how their student fees are being used, the project vision grew from a simple website
            to a dynamic application that shows updated information on all project-based activities
            of the organization.  Joining with fellow students Matthew Landen '17 and Josh Massey
            '18, the team worked on and <a href="http://devpost.com/software/sga-initiative-tracker">
                submitted an early version as part of HackUMBC</a>, UMBC's annual hackathon competiton,
            in the Fall 2014 semester.  Although it was not selected as a finalist for a prize, this
            initial version received much attention from the SGA administration.  Much work was done
            to improve both the public website and the behind-the-scenes work of the tool before a
            fully-functional public version was released in the Fall 2016 semester.  The Tracker is
            currently being put to a beta test with multiple scheduled improvements over the Fall
            semester and into the Spring.</p>
